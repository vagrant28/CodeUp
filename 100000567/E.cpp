#include <stdio.h>

int main(){
	double b, s;
	scanf("%lf", &b);
	if(b <= 100000){
		s = b * 0.1;
	}
	else if(b <= 200000){
		s = 100000 * 0.1 + (b - 100000) * 0.075;
	}
	else if(b <= 400000){
		s = 100000 * 0.1 + 100000 * 0.075 + (b - 200000) * 0.05;
	}
	else if(b <= 600000){
		s = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (s - 400000) * 0.03;
	}
	else if(b <= 1000000){
		s = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (b - 600000) * 0.015;
	}
	else{
		s = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (b - 1000000) * 0.01;
	}
	printf("%.2f\n", s);
}